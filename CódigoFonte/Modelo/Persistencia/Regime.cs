        /// <returns>Uma lista limitada de objetos ordenados pelo Nome</returns>
        public virtual IList<Regime> ConsultarOrdemAcendente(int qtd)
        {
            Regime ee = new Regime();
            ee.AdicionarFiltro(Filtros.SetOrderAsc("Nome"));
